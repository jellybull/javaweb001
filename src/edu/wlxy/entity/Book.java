package edu.wlxy.entity;

public class Book {
	private int bookid;
	private String bookname;
	private String bookauthor;
	private String bookpublish;
	private double bookprice;
	private String bookpubdate;
	private String booktype;
	private String bookimge;
	public int getBookid() {
		return bookid;
	}
	public void setBookid(int bookid) {
		this.bookid = bookid;
	}
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public String getBookauthor() {
		return bookauthor;
	}
	public void setBookauthor(String bookauthor) {
		this.bookauthor = bookauthor;
	}
	public String getBookpublish() {
		return bookpublish;
	}
	public void setBookpublish(String bookpublish) {
		this.bookpublish = bookpublish;
	}
	public double getBookprice() {
		return bookprice;
	}
	public void setBookprice(double bookprice) {
		this.bookprice = bookprice;
	}
	public String getBookpubdate() {
		return bookpubdate;
	}
	public void setBookpubdate(String bookpubdate) {
		this.bookpubdate = bookpubdate;
	}
	public String getBooktype() {
		return booktype;
	}
	public void setBooktype(String booktype) {
		this.booktype = booktype;
	}
	public String getBookimge() {
		return bookimge;
	}
	public void setBookimge(String bookimge) {
		this.bookimge = bookimge;
	}
}
